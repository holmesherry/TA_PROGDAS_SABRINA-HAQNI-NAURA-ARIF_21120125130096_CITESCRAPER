    <section class="styles-section">
        <h2 class="styles-heading">IEEE (Institute of Electrical and Electronics Engineers)</h2>
        <p class="styles-body">
            IEEE is a numeric citation style widely used in engineering, computer science, and technology fields.
            References are usually numbered in square brackets (for example: [1]) in the text, and the full reference
            appears in a numbered list at the end of the document. The order of the reference list follows the order
            of appearance in the text, not alphabetical order. IEEE emphasizes concise author names, article titles in
            quotation marks, and specific volume, issue, and page information.
        </p>
    </section>

    <section class="styles-section">
        <h2 class="styles-heading">APA (American Psychological Association)</h2>
        <p class="styles-body">
            APA is an author–date style commonly used in psychology, education, and social sciences. In‑text citations
            usually include the author’s last name and year (for example: (Smith, 2020)). The reference list is ordered
            alphabetically by author. APA focuses on year of publication to highlight how recent a source is, and it
            uses sentence case for article titles and italicized journal names.
            For example, an APA citation looks like this:
            	[1] Sapolsky, R. M. (2017). Behave: The biology of humans at our best and worst. Penguin Books.
                [2] Bologna, C. (2019, October 31). Why some people with anxiety love watching horror movies. HuffPost. https://www.huffpost.com/entry/anxiety-love-watching-horror-movies_l_5d277587e4b02a5a5d57b59e
        </p>
    </section>

    <section class="styles-section">
        <h2 class="styles-heading">MLA (Modern Language Association)</h2>
        <p class="styles-body">
            MLA is widely used in literature, arts, languages, and humanities. In‑text citations focus on the author and page
            number (for example: (Smith 45)), not the year. The Works Cited list is arranged alphabetically. MLA style
            typically uses title case for article titles and emphasizes page numbers as the main locator for quotations
            and ideas taken from sources.
        </p>
    </section>

    <section class="styles-section">
        <h2 class="styles-heading">Chicago Style</h2>
        <p class="styles-body">
            Chicago is widely used in history, social sciences, and humanities. Chicago style provides two main systems: Notes &amp; Bibliography (often used in history and humanities)
            and Author–Date (used in sciences and social sciences). In the Notes &amp; Bibliography system, citations
            appear in footnotes or endnotes, with a corresponding bibliography. In the Author–Date system, in‑text
            citations look similar to APA (author and year), but formatting details in the bibliography differ from APA.
        </p>
    </section>

    <section class="styles-section">
        <h2 class="styles-heading">Harvard Style</h2>
        <p class="styles-body">
            Harvard is another author–date style used in many universities and general academic writing. Widely used in business, humanities, and social sciences. It looks
            similar to APA at a glance, because both use the author’s last name and year in the text. However, the
            arrangement and punctuation in the reference list are slightly different, and Harvard often emphasizes the
            place of publication and publisher for books.
        </p>
    </section>

    <section class="styles-section">
        <h2 class="styles-heading">AMA (American Medical Association)</h2>
        <p class="styles-body">
            AMA is a numeric style used primarily in medicine, pharmacy, and health sciences. Sources are cited using
            superscript numbers in the text, and the reference list is numbered in the order of appearance. AMA has
            specific rules for abbreviating journal names and for writing author names, which are important in clinical
            and biomedical publications.
        </p>
    </section>

    <section class="styles-section">
        <h2 class="styles-heading">CSE (Council of Science Editors)</h2>
        <p class="styles-body">
            CSE is commonly used in chemistry, biology and natural sciences. It supports several systems, including Name–Year and
            Citation–Sequence. In the Name–Year system, in‑text citations look similar to APA or Harvard, while in the
            Citation–Sequence system, references are numbered according to their first appearance. CSE focuses heavily
            on scientific conventions and journal‑style formatting.
        </p>
    </section>

    <section class="styles-section">
        <h2 class="styles-heading">Bluebook</h2>
        <p class="styles-body">
            Bluebook is a legal citation style used in law schools, courts, and legal publications. It provides detailed
            rules for citing cases, statutes, regulations, and secondary legal sources. References usually follow the
            pattern of volume, reporter abbreviation, and page number, followed by the court and year in parentheses.
            Bluebook is very structured and precise to support legal argumentation.
        </p>
    </section>
